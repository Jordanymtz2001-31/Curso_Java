--USO DE CICLOS CON BLOQUES PL/SQL

DECLARE 
    LV_VARIABLE NUMBER := 0;
BEGIN 
    LOOP
        DBMS_OUTPUT.PUT_LINE('VALOR: ' || LV_VARIABLE);
        LV_VARIABLE := LV_VARIABLE + 1;
    
        --CONDICION PARA SALIR DE LA VARIABLE
        IF LV_VARIABLE = 11
            THEN EXIT;
        END IF;
    END LOOP;
END;
/

--USO DEL CICLO FOR
BEGIN 
    FOR i IN 1..10
        LOOP
            DBMS_OUTPUT.PUT_LINE('VALOR DE LA VARIABLE: '|| i);
            EXIT WHEN i = 6;
        END LOOP;
END;
/

--USO HILE Y LOOP
DECLARE 
    LV_ITERADOR NUMBER :=1;
BEGIN
    WHILE LV_ITERADOR <=6
        LOOP
            DBMS_OUTPUT.PUT_LINE('VALOR DE ITERADOR CON WHILE ' ||LV_ITERADOR);
            LV_ITERADOR := LV_ITERADOR + 1;
        END LOOP;
END;
/

--USO DE VARIABLES BOOLEANAS
DECLARE
    LV_VALIDADOR BOOLEAN := FALSE;
BEGIN 
    FOR I IN 1..20
        LOOP
            DBMS_OUTPUT.PUT_LINE('VALOR DEL VALIDADOR: '||I);
            IF I = 9 THEN
                LV_VALIDADOR := TRUE;
            END IF;
            EXIT WHEN LV_VALIDADOR;
        END LOOP;
END;
