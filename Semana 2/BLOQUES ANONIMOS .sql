--2. CREAR UN BLOQUE QUE IMPRIMA LAS TABLAS DE MUTIPLICAR DEL 1 AL 10
/* TABLA DEL 1
    1 * 1 = 1
*/
DECLARE 
    LV_RESULTADO NUMBER ;
BEGIN
    FOR i IN 1..10
        LOOP
            DBMS_OUTPUT.PUT_LINE('TABLA DEL : ' || i);
            FOR t IN 1..10
                LOOP
                    LV_RESULTADO := i * t;
                    DBMS_OUTPUT.PUT_LINE(i || '*'|| t ||'='|| LV_RESULTADO );
                END LOOP;
                DBMS_OUTPUT.PUT_LINE('-----------------------' );
        END LOOP;
END;
/

