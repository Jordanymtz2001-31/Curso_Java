--CALCULAR EL AREA DEUN CIRCULO
CREATE OR REPLACE FUNCTION AREA_CIRCULO(RADIO NUMBER) RETURN NUMBER
IS
BEGIN
    RETURN 3.1416 * (RADIO * RADIO);
END;
/

--LLAMAMOS POR BLOQUE ANONIMO
BEGIN DBMS_OUTPUT.PUT_LINE('EL AREA DEL CIRCULO ES: ' || AREA_CIRCULO(3));
END;
/



